/// The subgame number.
typedef SubgameNumber = int;

/// Reflects a round number during a subgame, 0 during role selection.
typedef RoundNumber = int;

/// Reflects the turn number during a round, i.e. in [0, playerNum-1]
typedef TurnNumber = int;

/// Reflects the index of the given player in the list of players.
typedef PlayerIndex = int;

/// Reflects the rank the given player achieved, where 1 is the best.
typedef PlayerRank = int;
